function FeatureNav({ features, activeId, onSelect }: { 
  features: Feature[]
  activeId: string
  onSelect: (id: string) => void 
}) {
  return (
    <div className="flex gap-4 mb-16 overflow-x-auto pb-4 justify-center">
      {features.map((feature) => (
        <button
          key={feature.id}
          onClick={() => onSelect(feature.id)}
          className={`flex flex-col items-center p-4 rounded-xl min-w-[100px] transition-all ${
            activeId === feature.id 
              ? 'bg-primary-rose text-white' 
              : 'hover:bg-background-secondary'
          }`}
        >
          <div className="w-8 h-8">{feature.icon}</div>
          <span className="text-sm mt-2 whitespace-nowrap">{feature.title}</span>
        </button>
      ))}
    </div>
  )
}

